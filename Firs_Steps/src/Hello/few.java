package Hello; 

public class few {
	public static void main(String args[]) {
		
		int x = 10;
		double y = 10;
		float z = 10;
		
		System.out.println("x = " + x);
		System.out.println("y = " + y);
		System.out.println("z = " + z);
		
		System.out.println();
		
		System.out.println("x/4 = " + x/4);
		System.out.println("y/4 = " + y/42);
		System.out.println("z/4 = " + z/42);
	}
}
